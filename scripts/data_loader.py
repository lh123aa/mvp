def load_industry_data(industry_name: str) -> Optional[Dict[str, Any]]:
    """加载指定行业的数据"""
    return INDUSTRIES.get(industry_name)


def get_all_industries() -> List[str]:
    """获取所有支持的行业列表"""
    return list(INDUSTRIES.keys())


def search_industries(keyword: str) -> List[str]:
    """根据关键词搜索行业"""
    keyword = keyword.lower()
    results = []
    for name in INDUSTRIES:
        if keyword in name:
            results.append(name)
    return results


def load_from_file(file_path: str) -> Optional[Dict[str, Any]]:
    """从JSON文件加载行业数据"""
    if not os.path.exists(file_path):
        return None
    
    try:
        with open(file_path, 'r', encoding='utf-8') as f:
            return json.load(f)
    except Exception:
        return None


